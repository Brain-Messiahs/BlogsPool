<p>PUBG Mobile India was officially announced on 12-Nov-2020 on their official Instagram handle. the day it was announced every PUBG lover felt very excitement.</p>



<p>PUBG Mobile India is an alternative of PUBG Mobile main version (owned by Tencent Games) so Indian govt. banned game in 2020 and thereafter <a href="https://en.wikipedia.org/wiki/PlayerUnknown%27s_Battlegrounds" target="_blank" rel="noreferrer noopener">PUBG</a> CORP (owner of PUBG) announced this India specific version for India region on their Instagram handle.         </p>



<p><strong> </strong>                                                    <strong>PUBG unban in India</strong> </p>



<p>Right now <a href="https://www.pubgmobile.in/" target="_blank" rel="noreferrer noopener">PUBG Mobile India</a> has registered its company in India and has already met all the conditions,(it has also appointed and Indian chairperson who is also chairman for other several outside India companies) due to which it was previously banned and also there is no Tencent Games involvement that is no CHINA involvement.</p>



